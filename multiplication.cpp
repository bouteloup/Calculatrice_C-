#include <iostream>
using namespace std;

/// \file	multiplication.cpp
/// \author	Rémy Bouteloup
/// \version	1.0
/// \date	13 février 2015
/// \brief	Multiplication de huit opérandes typée en int64_t, int32_t, float et double. 
/// \details	Ce fichier contient quatre fonctions nommées multiplication avec différentes variables en paramètres.

/// \brief Fonction de multiplication de deux nombres typée en int64_t.
/// Cette fonction possède deux variables de type int64_t en paramètres
/// \param operandeA est de type int64_t
/// \param operandeB est de type int64_t
/// \return Le résultat de la multiplication des deux opérandes en paramètres
int64_t multiplication(int64_t operandeA, int64_t operandeB)
{
return operandeA * operandeB;
}

/// \brief Fonction de multiplication de deux nombres typée en int32_t.
/// Cette fonction possède deux variables de type int32_t en paramètres
/// \param operandeA est de type int32_t
/// \param operandeB est de type int32_t
/// \return Le résultat de la multiplication des deux opérandes en paramètres
int32_t multiplication(int32_t operandeC, int32_t operandeD)
{
return operandeC * operandeD;
}

/// \brief Fonction de multiplication de deux nombres typée en float.
/// Cette fonction possède deux variables de type float en paramètres
/// \param operandeA est de type float
/// \param operandeB est de type float
/// \return Le résultat de la multiplication des deux opérandes en paramètres
float multiplication(float operandeE, float operandeF)
{
return operandeE * operandeF;
}

/// \brief Fonction de multiplication de deux nombres typée en double.
/// Cette fonction possède deux variables de type double en paramètres
/// \param operandeA est de type double
/// \param operandeB est de type double
/// \return Le résultat de la multiplication des deux opérandes en paramètres
double multiplication(double operandeG, double operandeH)
{
return operandeG * operandeH;
}


