#include <iostream>
using namespace std;

int64_t division(int64_t operandeA, int64_t operandeB)
{
return operandeA / operandeB;
}

int32_t division(int32_t operandeC, int32_t operandeD)
{
return operandeC / operandeD;
}

float division(float operandeE, float operandeF)
{
return operandeE / operandeF;
}

double division(double operandeG, double operandeH)
{
return operandeG / operandeH;
}

