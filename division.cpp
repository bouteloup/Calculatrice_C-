#include <iostream>
using namespace std;

/// \file	division.cpp
/// \author	Rémy Bouteloup
/// \version	1.0
/// \date	13 février 2015
/// \brief	Division de huit opérandes typée en int64_t, int32_t, float et double. 
/// \details	Ce fichier contient quatre fonctions nommées division avec différentes variables en paramètres.

/// \brief Fonction de division de deux nombres typée en int64_t.
/// Cette fonction possède deux variables de type int64_t en paramètres
/// \param operandeA est de type int64_t
/// \param operandeB est de type int64_t
/// \return Le résultat de la division des deux opérandes en paramètres
int64_t division(int64_t operandeA, int64_t operandeB)
{
return operandeA / operandeB;
}

/// \brief Fonction de division de deux nombres typée en int32_t.
/// Cette fonction possède deux variables de type int32_t en paramètres
/// \param operandeA est de type int32_t
/// \param operandeB est de type int32_t
/// \return Le résultat de la division des deux opérandes en paramètres
int32_t division(int32_t operandeC, int32_t operandeD)
{
return operandeC / operandeD;
}

/// \brief Fonction de division de deux nombres typée en float.
/// Cette fonction possède deux variables de type float en paramètres
/// \param operandeA est de type float
/// \param operandeB est de type float
/// \return Le résultat de la division des deux opérandes en paramètres
float division(float operandeE, float operandeF)
{
return operandeE / operandeF;
}

/// \brief Fonction de division de deux nombres typée en double.
/// Cette fonction possède deux variables de type double en paramètres
/// \param operandeA est de type double
/// \param operandeB est de type double
/// \return Le résultat de la division des deux opérandes en paramètres
double division(double operandeG, double operandeH)
{
return operandeG / operandeH;
}
